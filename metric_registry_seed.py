def _merge_clear_duplicate_canonicals(canonicals: dict[str, dict]) -> dict[str, dict]:
    merged = {canonical_id: dict(spec) for canonical_id, spec in canonicals.items()}
    for duplicate_id, survivor_id in _CLEAR_DUPLICATE_MERGES.items():
        if duplicate_id not in merged or survivor_id not in merged:
            continue
        duplicate = merged[duplicate_id]
        survivor = merged[survivor_id]

        survivor_aliases = list(survivor.get("aliases", []))
        alias_set = {str(alias).lower() for alias in survivor_aliases}
        duplicate_aliases = list(duplicate.get("aliases", []))
        duplicate_aliases.extend(
            [
                duplicate_id.replace("_", " "),
                str(duplicate.get("canonical_name") or "").lower(),
            ]
        )
        for alias in duplicate_aliases:
            alias_text = str(alias or "").strip()
            if alias_text and alias_text.lower() not in alias_set:
                survivor_aliases.append(alias_text)
                alias_set.add(alias_text.lower())
        survivor["aliases"] = survivor_aliases

        survivor_allowed_fact_classes = set(survivor.get("allowed_fact_classes", []))
        survivor_allowed_fact_classes.update(duplicate.get("allowed_fact_classes", []))
        survivor["allowed_fact_classes"] = list(survivor_allowed_fact_classes or ALL_FACT_CLASSES)

        survivor_allowed_unit_families = list(survivor.get("allowed_unit_families", []))
        survivor_unit_family_set = {str(item).lower() for item in survivor_allowed_unit_families}
        for unit_family in duplicate.get("allowed_unit_families", []):
            unit_text = str(unit_family or "")
            if unit_text.lower() not in survivor_unit_family_set:
                survivor_allowed_unit_families.append(unit_text)
                survivor_unit_family_set.add(unit_text.lower())
        survivor["allowed_unit_families"] = survivor_allowed_unit_families

        survivor["dimension_capable"] = bool(
            survivor.get("dimension_capable", False) or duplicate.get("dimension_capable", False)
        )
        del merged[duplicate_id]
    return merged


OPERATIONAL_CANONICALS = _merge_clear_duplicate_canonicals(OPERATIONAL_CANONICALS)


def build_alias_index(canonicals=OPERATIONAL_CANONICALS):
    idx = {}
    for canonical_id, spec in canonicals.items():
        idx[canonical_id.replace("_", " ")] = canonical_id
        for alias in spec["aliases"]:
            idx[alias.lower()] = canonical_id
    return idx


def _build_registry(canonicals=OPERATIONAL_CANONICALS):
    registry = []
    for canonical_id, spec in canonicals.items():
        entry = dict(spec)
        entry["canonical_id"] = canonical_id
        entry.setdefault("canonical_name", canonical_id.replace("_", " ").title())
        entry.setdefault("display_name", entry["canonical_name"])
        entry["aliases"] = list(entry.get("aliases", []))
        entry.setdefault("unit_family", "")
        entry["allowed_unit_families"] = list(entry.get("allowed_unit_families", []))
        entry.setdefault("unit", entry.get("unit_family", ""))
        entry.setdefault("graph_fact_type", "operational_metric")
        entry["dimension_capable"] = bool(entry.get("dimension_capable", False))
        entry["allowed_fact_classes"] = list(entry.get("allowed_fact_classes", ALL_FACT_CLASSES))
        entry.setdefault("scope_level", "unknown")
        entry.setdefault("canonical_definition", canonical_definition_for_entry(canonical_id, spec))
        entry.setdefault("category", "operational_seed")
        registry.append(entry)
    return registry


def _load_supplemental_registry_entries() -> list[dict]:
    entries: list[dict] = []
    for path in (
        Path("registry_semantic_overrides.json"),
        Path("registry_additions_approved.json"),
    ):
        if not path.exists():
            continue
        with path.open("r", encoding="utf-8") as handle:
            payload = json.load(handle)
        if isinstance(payload, dict):
            payload = payload.get("entries", [])
        if not isinstance(payload, list):
            continue
        entries.extend(dict(entry) for entry in payload if isinstance(entry, dict))
    return entries


def _merge_registry_entries(base_entries: list[dict], supplemental_entries: list[dict]) -> list[dict]:
    merged: dict[str, dict] = {
        str(entry.get("canonical_id") or ""): dict(entry)
        for entry in base_entries
        if entry.get("canonical_id")
    }
    for entry in supplemental_entries:
        canonical_id = str(entry.get("canonical_id") or "")
        if not canonical_id:
            continue
        current = dict(merged.get(canonical_id, {}))
        current.update(entry)
        if "aliases" in current:
            current["aliases"] = list(current.get("aliases") or [])
        merged[canonical_id] = current
    return list(merged.values())


REGISTRY = _merge_registry_entries(_build_registry(), _load_supplemental_registry_entries())
