def canonical_definition_for_entry(canonical_id: str, spec: dict) -> str:
    explicit = str(spec.get("canonical_definition") or "").strip()
    if explicit:
        return explicit

    canonical_key = str(canonical_id or "").strip()
    if canonical_key in _CANONICAL_DEFINITION_OVERRIDES:
        return _CANONICAL_DEFINITION_OVERRIDES[canonical_key]

    canonical_name = str(
        spec.get("canonical_name")
        or spec.get("display_name")
        or canonical_key.replace("_", " ")
    ).strip()
    primary_alias = str((spec.get("aliases") or [canonical_name])[0]).strip().lower()
    unit_family = str(spec.get("unit_family") or "").strip().lower()

    if unit_family == "count":
        return f"The number or count of {primary_alias} reported during the period."
    if unit_family == "percentage":
        return f"The percentage, rate, or share associated with {primary_alias}."
    if unit_family == "ratio":
        return f"A ratio-based measure of {primary_alias}."
    if unit_family in {"time", "duration"}:
        return f"The amount of time associated with {primary_alias}."
    if unit_family == "volume":
        return f"The volume associated with {primary_alias}."
    if unit_family == "weight":
        return f"The weight or mass associated with {primary_alias}."
    if unit_family == "monetary":
        return f"The monetary amount associated with {primary_alias}."
    if unit_family in {"per_unit", "per_area"}:
        return f"A normalized intensity measure associated with {primary_alias}."
    return f"A business metric measuring {primary_alias}."

